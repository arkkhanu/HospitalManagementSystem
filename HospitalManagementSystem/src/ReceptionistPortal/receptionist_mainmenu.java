    private void adddataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adddataActionPerformed
        
        Boolean emptyfield = false;
        int agee=-1;
        if(rr_patfname_et.getText().equals("")){ emptyfield=false;_fname_error.setVisible(true);}
        if(rr_patlname_et.getText().equals("")){ emptyfield=false;_lname_error.setVisible(true);}
        if(rr_patage_et.getText().equals("")){ emptyfield=false;_age_error.setVisible(true);_dob_error.setVisible(true);}
        if(rr_pataddress_et.getText().equals("")){ emptyfield=false;_address_error.setVisible(true);}
        if(rr_patphno_et.getText().equals("")){ emptyfield=false;_phnno_error.setVisible(true);}
        if(rr_patgender_combo.getSelectedItem().equals("Gender")){ emptyfield=false;_gender_error.setVisible(true);}
        if(rr_patcity_combo.getSelectedItem().equals("Select City")){ emptyfield=false;_city_error.setVisible(true);}
        
        try{
             agee=Integer.parseInt(rr_pataddress_et.getText());
            if(agee<18){
                emptyfield=false;
                _age_error.setVisible(true);
                _dob_error.setVisible(true);
            }
        }catch(NumberFormatException e){System.out.println(e);}
        
        if(!rr_patfname_et.getText().isEmpty()
           && !rr_patlname_et.getText().isEmpty()
           && !rr_patage_et.getText().isEmpty()
           && !rr_pataddress_et.getText().isEmpty()
           && !rr_patphno_et.getText().isEmpty()
           && !rr_patgender_combo.getSelectedItem().equals("Gender")
           && !rr_patcity_combo.getSelectedItem().equals("Select City")
           && (agee>=0 || agee<120)){emptyfield=true;}
        
        
        if(emptyfield==false){JOptionPane.showMessageDialog(null, "Please Fill the Fileds");}
        
        if(emptyfield==true){
            _address_error.setVisible(false);
            _age_error.setVisible(false);
            _fname_error.setVisible(false);
            _lname_error.setVisible(false);
            _phnno_error.setVisible(false);
            _gender_error.setVisible(false);
            _city_error.setVisible(false);
            _dob_error.setVisible(false);
             try{
                 conn.OpenConnection();
                String query="insert into RegPatient (fname,lname,age,dob,sexid,address,phno,cityid,regdate,rid) values("
                    +" '"+rr_patfname_et.getText()+"' , '"+rr_patlname_et.getText()+"' , "+rr_patage_et.getText()
                    +"  , to_date('"+dobirth+"','yyyy-MM-dd') , "+genderid+" , '"+rr_pataddress_et.getText()
                    +"' , '"+rr_patphno_et.getText()+"' , "+cityid+" , to_date('"+registrationdate.getText()+"','yyyy-MM-dd')"
                    +"  , "+rr_patrecp_tv.getText()+ ")";
                    
                int flag = conn.InsertUpdateDelete(query);
                if(flag==1){
                    JOptionPane.showMessageDialog(null,"Successfully Inserted..!");
                    rr_patid_tv.setText(conn.getID("select rpid from RegPatient ORDER BY rpid DESC Fetch first 1 rows only"));
                    rr_patfname_et.setText("");
                    rr_patlname_et.setText("");
                    rr_pataddress_et.setText("");
                    rr_pataddress_et.setText("");
                    rr_patphno_et.setText("");
                    rr_patcity_combo.setSelectedIndex(0);
                    rr_patgender_combo.setSelectedIndex(0);
                    rr_patdob_date.setDate(null);
                    rr_patage_et.setText("");
                    
                }
                conn.CloseConnection();
            }catch(HeadlessException  e ){System.out.println(e);}
            
        }
           
    }//GEN-LAST:event_adddataActionPerformed

