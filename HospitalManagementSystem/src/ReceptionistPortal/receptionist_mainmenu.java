    private void gettingdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gettingdateActionPerformed
        try{
            SimpleDateFormat t = new SimpleDateFormat("yyyy-MM-dd");
            dobirth = t.format(rr_patdob_date.getDate());
            System.out.println(dobirth);
            String today=registrationdate.getText();
            long diff = -1;
            Date d1 = t.parse(dobirth);
            Date d2 = t.parse(today);
            diff = d2.getYear()-d1.getYear();
            String totaldays = String.valueOf(diff);
            rr_patage_et.setText(totaldays);
            
            if(Integer.parseInt(rr_patage_et.getText()) <120 && Integer.parseInt(rr_patage_et.getText())>=0)
            {_age_error.setVisible(false);_dob_error.setVisible(false);}
            
            else{
            _age_error.setVisible(true);_dob_error.setVisible(true);
            }
        }catch(NullPointerException | ParseException e){System.out.println(e);_dob_error.setVisible(true);}
    }//GEN-LAST:event_gettingdateActionPerformed

    private void rr_patgender_comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rr_patgender_comboActionPerformed
        if(rr_patgender_combo.getSelectedItem().equals("Gender")){gender=null;}
        else{
            _gender_error.setVisible(false);
            gender=String.valueOf(rr_patgender_combo.getSelectedItem());
            try{
                conn.OpenConnection();
                String query= "select * from Gender";
                conn.GetData(query);
                while(conn.rst.next()){
                int    s1 = conn.rst.getInt("gid");
                String s2 = conn.rst.getString("gname");
                if(s2.equals(gender)){
                    genderid=s1;
//                    System.out.println(s1 + " : " + s2 + " : " + genderid); 
                  }
                }
                conn.CloseConnection();
            }catch(SQLException e){System.out.println(e);}
        }
    }//GEN-LAST:event_rr_patgender_comboActionPerformed

